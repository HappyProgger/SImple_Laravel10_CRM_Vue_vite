import './bootstrap';
import '../css/app.css';

import { createApp, h } from 'vue';
import { createInertiaApp } from '@inertiajs/vue3';
import { resolvePageComponent } from 'laravel-vite-plugin/inertia-helpers';
import { ZiggyVue } from '../../vendor/tightenco/ziggy/dist/vue.m';

const appName = import.meta.env.VITE_APP_NAME || 'Laravel';
import "../css/plugins/fontawesome-free/css/all.min.css";
import "../css/plugins/datatables-bs4/css/dataTables.bootstrap4.min.css";
import "../css/plugins/datatables-responsive/css/responsive.bootstrap4.min.css";
import "../css/plugins/datatables-buttons/css/buttons.bootstrap4.min.css";
import "../css/dist/css/alt/adminlte.min.css";

createInertiaApp({
    title: (title) => `${title} - ${appName}`,
    resolve: (name) => resolvePageComponent(`./Pages/${name}.vue`, import.meta.glob('./Pages/**/*.vue')),
    setup({ el, App, props, plugin }) {
        return createApp({ render: () => h(App, props) })
            .use(plugin)
            .use(ZiggyVue, Ziggy)
            .mount(el);
    },
    progress: {
        color: '#4B5563',
    },
});
